export const getAssetsByDepart = (departId: string) => {
  const depart = departs.find(d => d.id === departId);
  if (!depart) return [];
  
  return allAssets.filter(asset => depart.actifs.includes(asset.id));
};

export const getAssetsInRadius = (lat: number, lon: number, radiusKm: number) => {
  return allAssets.filter(asset => {
    const distance = calculateDistance(
      lat, lon,
      asset.geolocalisation.latitude,
      asset.geolocalisation.longitude
    );
    return distance <= radiusKm;
  });
};

// Fonction de calcul de distance (formule de Haversine)
function calculateDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371; // Rayon de la Terre en km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) * 
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

